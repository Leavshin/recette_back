CREATE TABLE IF NOT EXISTS public.account
(
    id_account integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    email character varying(255) COLLATE pg_catalog."default",
    is_admin boolean NOT NULL,
    name character varying(255) COLLATE pg_catalog."default",
    password character varying(255) COLLATE pg_catalog."default",
    preference character varying(255) COLLATE pg_catalog."default",
    CONSTRAINT account_pkey PRIMARY KEY (id_account),
    CONSTRAINT account_preference_check CHECK (preference::text = ANY (ARRAY['VEGETARIEN'::character varying, 'VEGAN'::character varying, 'SANS_LACTOSE'::character varying, 'SANS_GLUTEN'::character varying, 'AUCUNE'::character varying]::text[]))
)

TABLESPACE pg_default;

